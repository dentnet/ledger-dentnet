    parser_context_t blob = {.buffer = data, .bufferLen = static_cast<uint16_t>(size), .offset = 0};
    parser_context_t metadata = {.buffer = metadataBuffer, .bufferLen = metadataLen, .offset = 0};
    RegistryEntry_t tmpEntry = {0};
